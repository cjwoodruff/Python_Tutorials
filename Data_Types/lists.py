x = [1, 2, 3, 'Jon', 'Snow', True]
print(x)

# Displaying the indexing property of lists
print(x[0])
print(x[4])

# Displaying the append() method
x.append(4)
x.append(5)
x.append(6)
print(x)

# Displaying the pop() method
x.pop()
print(x)

# Display the del method
del x[3:5]
print(x)
del x[3]
print(x)
